// Defining the function:
function sum(num1, num2) {
  return num1 + num2;
}

function sub(num1,num2)
{
    return num1-num2;
}
function mul(num1,num2)
{
    return num1*num2;
}
function div(num1,num2)
{
    return num1/num2;
}
function mod(num1,num2)
{
    return num1%num2;
}

// Calling the function:
console.log(sum(3, 6)); 
console.log(sub(3, 6)); 
console.log(mul(3, 6)); 
console.log(div(3, 6)); 
console.log(mod(3, 6)); 
